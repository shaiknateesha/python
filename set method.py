a={'hii','hello','i','mm'}
b={'the','school','you','the'}
print("A:",a)
print("B:",b)
print()
#intersection method
ism=a.intersection(b)
print("intersection:",ism)
print()
#union method
um=a.union(b)
print("union:",um)
print()
#difference method
dm=a.difference(b)
print("difference:",dm)
print()
#issubset method
issub=a.issubset(b)
print("isssubset:",issub)
print()
#issuperset method
issup=a.issuperset(b)
print("issuperset:",issup)
print()